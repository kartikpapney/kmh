                    <p class="content_text" style="text-align:center; margin-top:-20px;">Rules and Regulation</p>

                    <ul class="content_text" style="text-align:justify">
                      <li>1. All the boarders of Kanchenjungha Men's Hostel shall be able to borrow books from the hostel library for a period of two weeks.</li>

                      <li>2. The membership of hostel library shall be valid till the date his name exists on the rolls of the university.</li>

                      <li>3. Members shall return all the books borrowed from hostel library atleast ten days before the end of the ongoing semester.</li>

                      <li>4. Before borrowing a book, boarders have to duly till all particulars in all the columns on the library register.</li>

                      <li>5. The library shall remain accessible to the boarders from 9.00 AM - 9.00 PM on all week days.</li>

                      <li>6. Donations in the form of new/used books from boarders will be highly appreciated.</li>

                      <li>7. A fine of Rs. 5/- per day shall be levied if a boarder fails to return a book after lapse of the permitted period.</li>

                      <li>8. In the event of loss, mutilation, spoilage or damage of a book borrowed from the hostel library, boarders will be liable to pay a sum equivalent to the price of the book or replenish the same book within a period of one month from the date of issue.</li>

                      <li>9. No guest of any boarder shall be allowed to borrow a book of the library.</li>

                      <li>10. The library should be kept clean and eatables except drinking water are not allowed in the library.</li>

                      <li>11. Boarders shall refrain from bringing their personal books to the library.</li>

                      <li>12. In the event of any dispute, the decision of the warden shall be final and binding.</li>
